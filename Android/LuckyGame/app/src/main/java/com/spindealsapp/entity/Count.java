public class Count {
    private long companies;
    private long gifts;
    private long offers;
    private long places;
    private long spins;

    public Count() {
    }

    public long getCompanies() {
        return companies;
    }

    public long getGifts() {
        return gifts;
    }

    public long getOffers() {
        return offers;
    }

    public long getPlaces() {
        return places;
    }

    public long getSpins() {
        return spins;
    }
}
